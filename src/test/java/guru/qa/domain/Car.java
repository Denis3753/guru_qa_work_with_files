package guru.qa.domain;

public class Car {

    private String mark;
    private String manufacturer;
    private String owner;
    private String[] models;

    public String getMark() {
        return mark;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public String getOwner() {
        return owner;
    }

    public String[] getModels() {
        return models;
    }




}
